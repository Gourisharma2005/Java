package basicJava;

public class EscapeSequence {
 public static void main(String[] args) {
	 System.out.println("hello\"everyone\"");
	 System.out.println("hello\\everyone\\");
	 System.out.println("hello\n everyone");
	 System.out.println("hello\t everyone");
	 System.out.println("hello\beveryone");
 }
}
